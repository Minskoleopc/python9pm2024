# logical operator 

# AND
# True   and   True  ----- True
# False  and   True  ----- False
# True   and   False ----- False
# False  and   False ----- False

print(6 == 6 and 7 == 7)
print(6 != 6 and 7 == 7)
print(6 == 6 and 7 != 7)
print(6 != 6 and 7 != 7)

# OR 
# True  or   True  ----- True 
# False or   True  ----- True
# True  or   False ----- True 
# False or   False  ----- False

print(6 == 6 or 7 == 7)
print(6 != 6 or 7 == 7)
print(6 == 6 or 7 != 7)
print(6 != 6 or 7 != 7)


